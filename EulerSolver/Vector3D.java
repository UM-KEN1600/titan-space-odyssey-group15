package EulerSolver;
/**
 * 3 dimensional vectors
 */
public class Vector3D {
    double x;
    double y;
    double z;

    public Vector3D()
    {
        this.x = 0;
        this.y = 0;
        this.z = 0;
    }

    public Vector3D(double x, double y, double z)
    {
        this.x = x;
        this.y = y;
        this.z = z;
    }

}
